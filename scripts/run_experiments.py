#!/usr/bin/env python3
"""Run SimpleScalar sim-outorder experiments and write structured results."""

from __future__ import annotations

import argparse
import csv
import datetime as dt
import json
import os
import re
import shlex
import shutil
import subprocess
import sys
import tempfile
import time
from pathlib import Path
from typing import Any


ROOT = Path(__file__).resolve().parents[1]
EXPERIMENTS_DIR = ROOT / "experiments"
STATS_MARKER = "sim: ** simulation statistics **"
OPTION_ORDER = [
    "fetch:ifqsize",
    "decode:width",
    "issue:width",
    "issue:inorder",
    "ruu:size",
    "commit:width",
    "lsq:size",
    "res:ialu",
    "res:imult",
    "res:fpalu",
    "res:fpmult",
    "res:memport",
    "bpred",
]
CSV_FIELDS = [
    "benchmark",
    "experiment",
    "task",
    "status",
    "returncode",
    "timed_out",
    "duration_sec",
    "sim_num_insn",
    "sim_cycle",
    "sim_CPI",
    "sim_IPC",
    "sim_num_refs",
    "sim_num_branches",
    "bpred_dir_rate",
    "bpred_misses",
    "il1.miss_rate",
    "dl1.miss_rate",
]


def read_json(path: Path) -> dict[str, Any]:
    with path.open("r", encoding="utf-8") as handle:
        return json.load(handle)


def write_json(path: Path, payload: dict[str, Any]) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    with path.open("w", encoding="utf-8") as handle:
        json.dump(payload, handle, indent=2, ensure_ascii=False, sort_keys=True)
        handle.write("\n")


def now_iso() -> str:
    return dt.datetime.now(dt.timezone.utc).replace(microsecond=0).isoformat()


def safe_name(value: str) -> str:
    return re.sub(r"[^A-Za-z0-9_.-]+", "_", value).strip("_")


def parse_bool(value: str) -> bool:
    return value.lower() in {"1", "true", "yes", "y", "on"}


def resolve_selection(raw: str, sets: dict[str, list[str]], available: set[str], kind: str) -> list[str]:
    selected: list[str] = []
    tokens = [token.strip() for token in raw.split(",") if token.strip()]
    if not tokens:
        raise ValueError(f"No {kind} selected")
    for token in tokens:
        if token in sets:
            selected.extend(sets[token])
        elif token in available:
            selected.append(token)
        else:
            known = ", ".join(sorted(set(sets) | available))
            raise ValueError(f"Unknown {kind} '{token}'. Known values: {known}")
    unique: list[str] = []
    seen: set[str] = set()
    for item in selected:
        if item not in available:
            raise ValueError(f"{kind} set references unknown item '{item}'")
        if item not in seen:
            unique.append(item)
            seen.add(item)
    return unique


def value_to_text(value: Any) -> str:
    if isinstance(value, bool):
        return "true" if value else "false"
    return str(value)


def option_items(options: dict[str, Any]) -> list[tuple[str, Any]]:
    ordered = [(name, options[name]) for name in OPTION_ORDER if name in options]
    extras = sorted((name, value) for name, value in options.items() if name not in OPTION_ORDER)
    return ordered + extras


def load_base_cache_config(benchmark: dict[str, Any]) -> str:
    config_path = ROOT / benchmark["directory"] / "config_example_assoc1_direct"
    if not config_path.exists():
        raise FileNotFoundError(f"Missing cache config: {config_path}")
    return config_path.read_text(encoding="utf-8").strip()


def render_config(benchmark: dict[str, Any], experiment: dict[str, Any]) -> str:
    lines = [
        "# Generated by scripts/run_experiments.py",
        "# Base cache/TLB configuration supplied with the benchmark bundle.",
        load_base_cache_config(benchmark),
        "",
        "# Experiment-specific sim-outorder configuration.",
    ]
    for name, value in option_items(experiment.get("options", {})):
        lines.append(f"-{name} {value_to_text(value)}")
    return "\n".join(lines) + "\n"


def stat_value(raw: str) -> int | float | str:
    if raw.startswith("<"):
        return raw
    try:
        if re.fullmatch(r"[-+]?\d+", raw):
            return int(raw)
        return float(raw)
    except ValueError:
        return raw


def parse_stats(stdout: str) -> dict[str, int | float | str]:
    marker_index = stdout.find(STATS_MARKER)
    if marker_index == -1:
        return {}
    stats: dict[str, int | float | str] = {}
    for line in stdout[marker_index + len(STATS_MARKER) :].splitlines():
        stripped = line.strip()
        if not stripped or stripped.startswith("#"):
            continue
        match = re.match(r"^([A-Za-z0-9_.]+)\s+([^\s#]+)", stripped)
        if not match:
            continue
        stats[match.group(1)] = stat_value(match.group(2))
    return stats


def first_numeric_stat(stats: dict[str, Any], suffix: str) -> int | float | None:
    for key in sorted(stats):
        if key.endswith(suffix) and isinstance(stats[key], (int, float)):
            return stats[key]
    return None


def csv_row(run: dict[str, Any]) -> dict[str, Any]:
    stats = run.get("stats", {})
    row = {field: "" for field in CSV_FIELDS}
    row.update(
        {
            "benchmark": run["benchmark"],
            "experiment": run["experiment"],
            "task": run["task"],
            "status": run["status"],
            "returncode": run.get("returncode", ""),
            "timed_out": run.get("timed_out", False),
            "duration_sec": run.get("duration_sec", ""),
        }
    )
    for field in [
        "sim_num_insn",
        "sim_cycle",
        "sim_CPI",
        "sim_IPC",
        "sim_num_refs",
        "sim_num_branches",
        "il1.miss_rate",
        "dl1.miss_rate",
    ]:
        row[field] = stats.get(field, "")
    row["bpred_dir_rate"] = first_numeric_stat(stats, "bpred_dir_rate") or ""
    row["bpred_misses"] = first_numeric_stat(stats, "misses") or ""
    return row


def build_command(
    sim_bin: str,
    config_path: Path,
    program_output: Path,
    benchmark: dict[str, Any],
    max_instructions: int,
) -> list[str]:
    command = [sim_bin]
    if max_instructions > 0:
        command.extend(["-max:inst", str(max_instructions)])
    command.extend(["-redir:prog", str(program_output), "-config", str(config_path)])
    command.append(benchmark["program"])
    command.extend(str(arg) for arg in benchmark.get("args", []))
    return command


def run_one(
    sim_bin: str,
    benchmark_name: str,
    benchmark: dict[str, Any],
    experiment_id: str,
    experiment: dict[str, Any],
    output: Path,
    max_instructions: int,
    timeout_sec: int,
    dry_run: bool,
) -> dict[str, Any]:
    run_dir = output / "runs" / safe_name(benchmark_name) / safe_name(experiment_id)
    run_dir.mkdir(parents=True, exist_ok=True)
    config_path = run_dir / "sim-outorder.cfg"
    stdout_path = run_dir / "stdout.txt"
    stderr_path = run_dir / "stderr.txt"
    program_output = run_dir / "program.out"
    config_path.write_text(render_config(benchmark, experiment), encoding="utf-8")

    command = build_command(sim_bin, config_path, program_output, benchmark, max_instructions)
    source_dir = ROOT / benchmark["directory"]
    stdin_handle = None

    started_at = now_iso()
    start = time.monotonic()
    timed_out = False
    returncode: int | None = None
    stdout = ""
    stderr = ""
    status = "planned" if dry_run else "failed"

    try:
        if dry_run:
            status = "planned"
        else:
            with tempfile.TemporaryDirectory(prefix="sim-outorder-") as temp_dir:
                cwd = Path(temp_dir) / "benchmark"
                shutil.copytree(
                    source_dir,
                    cwd,
                    ignore=shutil.ignore_patterns(".DS_Store", "__pycache__"),
                )
                if benchmark.get("stdin"):
                    stdin_handle = (cwd / benchmark["stdin"]).open("rb")
                completed = subprocess.run(
                    command,
                    cwd=cwd,
                    stdin=stdin_handle,
                    stdout=subprocess.PIPE,
                    stderr=subprocess.PIPE,
                    timeout=timeout_sec if timeout_sec > 0 else None,
                    check=False,
                )
                returncode = completed.returncode
                stdout = completed.stdout.decode("utf-8", errors="replace")
                stderr = completed.stderr.decode("utf-8", errors="replace")
                status = "completed" if returncode == 0 else "failed"
    except subprocess.TimeoutExpired as exc:
        timed_out = True
        status = "timed_out"
        returncode = None
        stdout = (exc.stdout or b"").decode("utf-8", errors="replace")
        stderr = (exc.stderr or b"").decode("utf-8", errors="replace")
    finally:
        if stdin_handle is not None:
            stdin_handle.close()

    duration_sec = round(time.monotonic() - start, 3)
    stdout_path.write_text(stdout, encoding="utf-8", errors="replace")
    stderr_path.write_text(stderr, encoding="utf-8", errors="replace")

    stats = parse_stats(stdout)
    run = {
        "benchmark": benchmark_name,
        "benchmark_family": benchmark["family"],
        "benchmark_input": benchmark.get("input"),
        "experiment": experiment_id,
        "experiment_title": experiment["title"],
        "task": experiment["task"],
        "status": status,
        "returncode": returncode,
        "timed_out": timed_out,
        "duration_sec": duration_sec,
        "started_at": started_at,
        "ended_at": now_iso(),
        "max_instructions": max_instructions,
        "command": command,
        "command_text": shlex.join(command),
        "source_workdir": str(source_dir.relative_to(ROOT)),
        "isolated_workdir": True,
        "options": experiment.get("options", {}),
        "stats": stats,
        "files": {
            "config": str(config_path.relative_to(output)),
            "stdout": str(stdout_path.relative_to(output)),
            "stderr": str(stderr_path.relative_to(output)),
            "program_output": str(program_output.relative_to(output)),
        },
    }
    write_json(run_dir / "result.json", run)
    return run


def write_csv(path: Path, runs: list[dict[str, Any]]) -> None:
    with path.open("w", encoding="utf-8", newline="") as handle:
        writer = csv.DictWriter(handle, fieldnames=CSV_FIELDS)
        writer.writeheader()
        for run in runs:
            writer.writerow(csv_row(run))


def build_parser() -> argparse.ArgumentParser:
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument("--benchmarks", default="quick", help="Benchmark set/name list, e.g. quick, all, GCC_4,LI_2")
    parser.add_argument("--experiment-set", default="smoke", help="Experiment set/name list, e.g. smoke, task1, assignment")
    parser.add_argument("--output", default="results/latest", help="Output directory")
    parser.add_argument("--sim-bin", default=os.environ.get("SIM_OUTORDER_BIN", "sim-outorder"), help="sim-outorder executable")
    parser.add_argument("--max-instructions", type=int, default=100000, help="Instruction cap; use 0 for complete benchmark runs")
    parser.add_argument("--timeout-sec", type=int, default=0, help="Per-run timeout; use 0 for no timeout")
    parser.add_argument("--dry-run", action="store_true", help="Write planned runs without executing sim-outorder")
    parser.add_argument("--list", action="store_true", help="List available benchmark and experiment sets")
    return parser


def main(argv: list[str] | None = None) -> int:
    args = build_parser().parse_args(argv)
    benchmark_doc = read_json(EXPERIMENTS_DIR / "benchmarks.json")
    benchmark_set_doc = read_json(EXPERIMENTS_DIR / "benchmark_sets.json")
    experiment_doc = read_json(EXPERIMENTS_DIR / "experiment_sets.json")
    benchmarks = benchmark_doc["benchmarks"]
    experiments = experiment_doc["experiments"]

    if args.list:
        print("Benchmark sets:")
        for name in sorted(benchmark_set_doc["sets"]):
            print(f"  {name}: {', '.join(benchmark_set_doc['sets'][name])}")
        print("Experiment sets:")
        for name in sorted(experiment_doc["sets"]):
            print(f"  {name}: {', '.join(experiment_doc['sets'][name])}")
        return 0

    try:
        selected_benchmarks = resolve_selection(
            args.benchmarks,
            benchmark_set_doc["sets"],
            set(benchmarks),
            "benchmark",
        )
        selected_experiments = resolve_selection(
            args.experiment_set,
            experiment_doc["sets"],
            set(experiments),
            "experiment",
        )
    except ValueError as exc:
        print(f"error: {exc}", file=sys.stderr)
        return 2

    output = (ROOT / args.output).resolve() if not Path(args.output).is_absolute() else Path(args.output)
    output.mkdir(parents=True, exist_ok=True)
    runs: list[dict[str, Any]] = []
    started_at = now_iso()

    for benchmark_name in selected_benchmarks:
        benchmark = benchmarks[benchmark_name]
        for experiment_id in selected_experiments:
            experiment = experiments[experiment_id]
            print(f"[{benchmark_name}] {experiment_id}: {experiment['title']}", flush=True)
            run = run_one(
                args.sim_bin,
                benchmark_name,
                benchmark,
                experiment_id,
                experiment,
                output,
                args.max_instructions,
                args.timeout_sec,
                args.dry_run,
            )
            runs.append(run)

    aggregate = {
        "schema_version": 1,
        "generated_at": now_iso(),
        "started_at": started_at,
        "repo_root": str(ROOT),
        "sim_bin": args.sim_bin,
        "max_instructions": args.max_instructions,
        "timeout_sec": args.timeout_sec,
        "benchmark_selection": args.benchmarks,
        "experiment_selection": args.experiment_set,
        "selected_benchmarks": selected_benchmarks,
        "selected_experiments": selected_experiments,
        "benchmarks": {name: benchmarks[name] for name in selected_benchmarks},
        "experiments": {name: experiments[name] for name in selected_experiments},
        "runs": runs,
    }
    write_json(output / "results.json", aggregate)
    write_csv(output / "results.csv", runs)

    failed = [run for run in runs if run["status"] not in {"completed", "planned"}]
    if failed:
        print(f"{len(failed)} run(s) failed or timed out. See {output / 'results.json'}", file=sys.stderr)
        return 1
    print(f"Wrote {len(runs)} run(s) to {output}")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
